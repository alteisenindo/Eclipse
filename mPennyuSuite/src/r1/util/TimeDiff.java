package r1.util;

public class TimeDiff 
{
	long lDifSeconds;
	long lDifMinutes;
	long lDifHours;
	long lDifDays;
	
	public TimeDiff()
	{}

	public long getlDifSeconds() {
		return lDifSeconds;
	}

	public void setlDifSeconds(long lDifSeconds) {
		this.lDifSeconds = lDifSeconds;
	}

	public long getlDifMinutes() {
		return lDifMinutes;
	}

	public void setlDifMinutes(long lDifMinutes) {
		this.lDifMinutes = lDifMinutes;
	}

	public long getlDifHours() {
		return lDifHours;
	}

	public void setlDifHours(long lDifHours) {
		this.lDifHours = lDifHours;
	}

	public long getlDifDays() {
		return lDifDays;
	}

	public void setlDifDays(long lDifDays) {
		this.lDifDays = lDifDays;
	}	
}
